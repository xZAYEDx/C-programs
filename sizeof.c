//program to use sizeof() operator 
#include<stdio.h>
void main()
{
     char a;
     float b;
     double c;
     long double d;

     printf("\n size of char = %d", sizeof(a));
     printf("\n size of float = %d", sizeof(b));
     printf("\n size of double = %d", sizeof(c));
     printf("\n size of long double = %d", sizeof(d));
}